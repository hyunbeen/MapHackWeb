	private String map_alarm;
	public String getMap_ilocation() {
		return map_ilocation;
	}
	public void setMap_ilocation(String map_ilocation) {
		this.map_ilocation = map_ilocation;
	}
	public String getMap_alarm() {
		return map_alarm;
	}
	public void setMap_alarm(String map_alarm) {
		this.map_alarm = map_alarm;
	}
	public String getMap_time() {
		return map_time;
	}
	public void setMap_time(String map_time) {
		this.map_time = map_time;
	}
	public String getMap_text() {
		return map_text;
	}
	public void setMap_text(String map_text) {
		this.map_text = map_text;
	}
	private String map_time;
	private String map_text;
