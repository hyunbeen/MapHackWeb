package maphack.infodetail.dto;

public class InfoReview {
	private String IrNum;
	private String IrLocation;
	private String IrMid;
	private String IrContent;
	private String IrDate;
	private int IrStar;
	
	
	public String getIrNum() {
		return IrNum;
	}
	public void setIrNum(String irNum) {
		IrNum = irNum;
	}
	public String getIrLocation() {
		return IrLocation;
	}
	public void setIrLocation(String irLocation) {
		IrLocation = irLocation;
	}
	public String getIrMid() {
		return IrMid;
	}
	public void setIrMid(String irMid) {
		IrMid = irMid;
	}
	public String getIrContent() {
		return IrContent;
	}
	public void setIrContent(String irContent) {
		IrContent = irContent;
	}
	public String getIrDate() {
		return IrDate;
	}
	public void setIrDate(String irDate) {
		IrDate = irDate;
	}
	public int getIrStar() {
		return IrStar;
	}
	public void setIrStar(int irStar) {
		IrStar = irStar;
	}
	
	
	
	
	
}
